import torch.nn as nn
import torch

def autopad(k, p=None):  # kernel, padding
    # Pad to 'same'
    if p is None:
        if isinstance(k, int) :
            p = k // 2
        else :
           p= [x // 2 for x in k]
    return p
class Conv(nn.Module):    #yolo算法中的标准卷积，   #卷积->归一化->Silu激活函数
    def __init__(self, c1, c2, k=1, s=1, p=None, g=1, act=True):   # 输入通道, 输出通道, 卷积核大小, 卷积核步长, pad的数目, 控制分组卷积（默认为1，即不进行分组）,是否进行激活函数的操作
        super(Conv, self).__init__()
        self.conv = nn.Conv2d(c1, c2, k, s, autopad(k, p), groups=g, bias=False)    #bias 是指是否学习偏差
        self.bn = nn.BatchNorm2d(c2)   #归一化
        self.act = nn.SiLU() if act is True else (act if isinstance(act, nn.Module) else nn.Identity())

    def forward(self, x):
        return self.act(self.bn(self.conv(x)))

    def fuseforward(self, x):
        return self.act(self.conv(x))    #省略归一化操作的前向传播

class Yolov7_E_ELAN(nn.Module):
    def __init__(self, inc, ouc, hidc, act=True):
        super(Yolov7_E_ELAN, self).__init__()

        self.conv1 = Conv(inc, hidc, k=1, act=act)
        self.conv2 = Conv(inc, hidc, k=1, act=act)
        self.conv3 = Conv(hidc, hidc, k=3, act=act)
        self.conv4 = Conv(hidc, hidc, k=3, act=act)
        self.conv5 = Conv(hidc, hidc, k=3, act=act)
        self.conv6 = Conv(hidc, hidc, k=3, act=act)
        self.conv7 = Conv(hidc * 4, ouc, k=1, act=act)

    def forward(self, x):
        x1, x2 = self.conv1(x), self.conv2(x)
        x3 = self.conv3(x2)
        x4 = self.conv4(x3)
        x5 = self.conv5(x4)
        x6 = self.conv6(x5)
        x_concat = torch.concat([x1, x2, x4, x6], dim=1)
        x_final = self.conv7(x_concat)
        return x_final


